<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <link rel="stylesheet" href="register.css">
    <title>Login</title>
</head>
<body>
    <div id="notification" class="notification"></div> <!-- Notification container -->
    
    <div class="header">
        <img src="assets/Baze Logo.png" alt="Baze University Logo">
        <h2>IDEAS/24/15429</h2>
    </div>

    <div class="container">
        <h2>Login</h2>
        <form method="POST" action="login_secure.php">
            <label for="email">Email:</label>
            <input type="text" name="email" required>
            
            <label for="password">Password:</label>
            <input type="password" name="password" required>
            
            <button type="submit">Login</button>
        </form>
        <p>Don't have an account? <a href="register.php">Register here</a></p>
    </div>

    <!-- Define the showNotification function globally -->
    <script>
    function showNotification(message, type = 'success') {
        var notification = document.getElementById('notification');
        
        // Set the message and the notification type
        notification.innerHTML = message;
        notification.classList.add('show');
        
        // Apply error styling if type is 'error'
        if (type === 'error') {
            notification.classList.add('error');
        } else {
            notification.classList.remove('error');
        }
        
        // Hide the notification after 4 seconds
        setTimeout(function() {
            notification.classList.remove('show');
        }, 4000);
    }
    </script>

    <!-- Place the notification script generated by PHP -->
    <?php echo $showNotificationScript; ?>
</body>
