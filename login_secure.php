<?php
session_start();
include 'db_connect.php'; // Include the connection file

$showNotificationScript = '';

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $email = $_POST["email"];
    $password = $_POST["password"];

    // Secure version using prepared statements (SQL injection protection)
    $stmt = $conn->prepare("SELECT * FROM users WHERE email = ?");
    $stmt->bind_param("s", $email);
    $stmt->execute();
    $result = $stmt->get_result();

    if ($result->num_rows > 0) {
        $user = $result->fetch_assoc();

        // Verify the password using password_verify (if password is hashed)
        if (password_verify($password, $user['password'])) {
            // Set session variables and redirect to the profile page
            $_SESSION["user_id"] = $user["id"];
            $_SESSION["username"] = $user["username"];
            header("Location: home.php");
            exit();
        } else {
            // Invalid password
            $showNotificationScript = "<script>
                    document.addEventListener('DOMContentLoaded', function() {
                        showNotification('Invalid password.', 'error');
                    });
                  </script>";
        }
    } else {
        // No account found with that email
        $showNotificationScript = "<script>
                document.addEventListener('DOMContentLoaded', function() {
                    showNotification('No account found with that email.', 'error');
                });
              </script>";
    }

    $stmt->close();
}

$conn->close();
?>

<!DOCTYPE html>
